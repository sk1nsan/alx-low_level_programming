/**
 * op_add - adds 2 numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs 2 numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul 2 numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod 2 numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
